### Text formatting ###

export CLEAR="\e[0m"

# Text settings.
export BOLD="\e[1m"
export ITALIC="\e[3m"
export UNDERLINE="\e[4m"

# Text color.
export RED="\e[31m"
export GREEN="\e[32m"
export YELLOW="\e[33m"
export BLUE="\e[34m"
export MAGENTA="\e[35m"
export CYAN="\e[36m"

# Text color with bold font.
export RED_BOLD="\e[1;31m"
export GREEN_BOLD="\e[1;32m"
export YELLOW_BOLD="\e[1;33m"
export BLUE_BOLD="\e[1;34m"
export MAGENTA_BOLD="\e[1;35m"
export CYAN_BOLD="\e[1;36m"

# Background color.
export RED_BG="\e[41m"
export GREEN_BG="\e[42m"
export YELLOW_BG="\e[43m"
export BLUE_BG="\e[44m"
export MAGENTA_BG="\e[45m"
export CYAN_BG="\e[46m"

# Background color with bold font.
export RED_BG_BOLD="\e[1;41m"
export GREEN_BG_BOLD="\e[1;42m"
export YELLOW_BG_BOLD="\e[1;43m"
export BLUE_BG_BOLD="\e[1;44m"
export MAGENTA_BG_BOLD="\e[1;45m"
export CYAN_BG_BOLD="\e[1;46m"

